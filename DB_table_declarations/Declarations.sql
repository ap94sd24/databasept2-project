CREATE TABLE REVIEW_SES(
	SECTIONID INT NOT NULL,
	R_DATE DATE NOT NULL,
	R_TIME TIME NOT NULL	
);

CREATE TABLE COURSES(
    COURSE_NO INT NOT NULL,
    UNITS INT NOT NULL,
    TYPE VARCHAR(20) NOT NULL,
    GRADE VARCHAR(20),
    LAB_REQ BOOLEAN NOT NULL,
    PREREQ VARCHAR(10) NOT NULL,
    CONSTRAINT PK_COURSE PRIMARY KEY(COURSE_NO)
);

CREATE TABLE CLASSES (
	CID INT  NOT NULL references COURSES(CID), 
	TITLE VARCHAR(20) NOT NULL, 
	QUARTER INT NOT NULL, 
	YEAR INT NOT NULL, 
	REVIEWSESSDATE INT NOT NULL, 
	REVIEWSESSTIME INT NOT NULL,
	CONSTRAINT PK_STD4 PRIMARY KEY(TITLE)
);
 
CREATE TABLE SECTION (
	SECTID INT NOT NULL,
	TITLE VARCHAR(20) NOT NULL references CLASSES(TITLE), 
	LECTIME INT NOT NULL, 
	LECDATE INT NOT NULL, 
	DISCUSSDATE INT NOT NULL, 
	DISCUSSTIME INT NOT NULL, 
	BUILDING VARCHAR(20) NULL, 
	ROOM VARCHAR(20)  NULL,
	MAXCAP INT NULL,
	CONSTRAINT PK_STD2 PRIMARY KEY(SECTID)
); 

CREATE TABLE PROBATION(
    SSN VARCHAR(20) NOT NULL,
    PERIOD VARCHAR(20),
    REASON BOOLEAN NOT NULL,
);
